TERM_WIDTH=$(tput cols 2>/dev/null || echo 80)

function print() {
    for line in "$@"; do
        line_length=$(echo "$line" | wc -c)
        pad=$((TERM_WIDTH - line_length))
        [ "$pad" -lt 0 ] && pad=0
        printf "${TEXT_COLOR}${BG_GREEN}%s%*s${NC}\n" "$line" "$pad" ""
    done
}

function printHeader() {
    local title="$1"
    print " "
    print "==============================================================================="
    print " $title"
    print "==============================================================================="
    print " "
}

